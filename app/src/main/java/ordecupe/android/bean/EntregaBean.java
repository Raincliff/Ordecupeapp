package ordecupe.android.bean;

public class EntregaBean {

    private int codEnt;
    private String tipEnt;
    private String disEnt;
    private double preEnt;


    public int getCodEnt() {
        return codEnt;
    }

    public void setCodEnt(int codEnt) {
        this.codEnt = codEnt;
    }

    public String getTipEnt() {
        return tipEnt;
    }

    public void setTipEnt(String tipEnt) {
        this.tipEnt = tipEnt;
    }

    public String getDisEnt() {
        return disEnt;
    }

    public void setDisEnt(String disEnt) {
        this.disEnt = disEnt;
    }

    public double getPreEnt() {
        return preEnt;
    }

    public void setPreEnt(double preEnt) {
        this.preEnt = preEnt;
    }
}
