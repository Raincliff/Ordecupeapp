package ordecupe.android.bean;

public class ProductoBean {
    private int idPro;
    private String nomPro;
    private String marPro;
    private String tipPro;
    private double prePro;
    private int minPro;
    private int stoPro;


    public int getIdPro() {
        return idPro;
    }

    public void setIdPro(int idPro) {
        this.idPro = idPro;
    }

    public String getNomPro() {
        return nomPro;
    }

    public void setNomPro(String nomPro) {
        this.nomPro = nomPro;
    }

    public String getMarPro() {
        return marPro;
    }

    public void setMarPro(String marPro) {
        this.marPro = marPro;
    }

    public String getTipPro() {
        return tipPro;
    }

    public void setTipPro(String tipPro) {
        this.tipPro = tipPro;
    }

    public double getPrePro() {
        return prePro;
    }

    public void setPrePro(double prePro) {
        this.prePro = prePro;
    }

    public int getMinPro() {
        return minPro;
    }

    public void setMinPro(int minPro) {
        this.minPro = minPro;
    }

    public int getStoPro() {
        return stoPro;
    }

    public void setStoPro(int stoPro) {
        this.stoPro = stoPro;
    }
}
